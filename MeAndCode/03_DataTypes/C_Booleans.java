// Java Boolean Data Types - Example

public class C_Booleans {
    public static void main(String[] args) {

        // Boolean variables
        boolean isJavaFun = true;
        boolean isFishTasty = false;

        // Printing Boolean values
        System.out.println("Is Java fun? " + isJavaFun);
        System.out.println("Is fish tasty? " + isFishTasty);

        System.out.println("----------------------------");

        // Boolean expressions
        int x = 10;
        int y = 20;

        boolean comparison1 = x < y; // true
        boolean comparison2 = x > y; // false

        System.out.println("Is x less than y? " + comparison1);
        System.out.println("Is x greater than y? " + comparison2);
    }
}
// Java Data Types - Real-Life Example

public class E_Examples {
  public static void main(String[] args) {
      // Create variables of different data types
      int items = 50;              // Integer (number of items)
      float costPerItem = 9.99f;   // Floating point number (cost per item)
      float totalCost = items * costPerItem; // Calculating total cost
      char currency = '$';         // Character (currency symbol)

      // Print variables
      System.out.println("Number of items: " + items);
      System.out.println("Cost per item: " + currency + costPerItem);
      System.out.println("Total cost = " + currency + totalCost);
  }
}
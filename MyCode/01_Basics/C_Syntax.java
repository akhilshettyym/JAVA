public class C_Syntax {   // Every Java program must have a class, and its name should match the filename
public static void main(String[] args) {        // The main() method is the entry point of any Java program
    // Java Syntax Example - Hello World Program
    
        // System.out.println() is used to print text to the console
        System.out.println("Hello World"); // Output: Hello World

    } // End of main() method
} // End of class


/*
 
Class Definition (public class Main)

Every Java program must be inside a class.
The class name must match the filename (Main.java).
Java is case-sensitive, meaning Main and main are different.
Main Method (public static void main(String[] args))

This is the entry point of any Java application.
public → The method is accessible from anywhere.
static → The method belongs to the class and doesn't require an object to be called.
void → The method does not return any value.
String[] args → Used to accept command-line arguments.
Printing Output (System.out.println("Hello World"))

System → A built-in Java class that provides system-related functions.
out → Represents standard output (console).
println() → Prints the given text and moves to the next line.
Each statement in Java must end with a semicolon (;).


 */
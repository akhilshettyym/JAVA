// Java Comments Example - Demonstrating Single-line and Multi-line Comments

public class E_Comments {
    public static void main(String[] args) {

        // Single-line comment: This comment is ignored by Java
        System.out.println("Hello World"); // This prints "Hello World" to the screen

        // Another single-line comment explaining the next statement
        System.out.println("Java is fun!"); // Printing another message

        /*
         * Multi-line comment:
         * The following statement prints "Learning Java" to the console.
         * This is useful for longer explanations.
         */
        System.out.println("Learning Java");

        /*
         * Multi-line comments can also be used
         * to temporarily disable a block of code.
         * Uncomment the next line to see its output.
         */
        // System.out.println("This line is commented out and will not execute.");
    }
}
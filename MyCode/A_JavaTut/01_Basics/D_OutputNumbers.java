

public class D_OutputNumbers {
    public static void main(String[] args) {

        // Printing numbers directly without double quotes
        System.out.println(3);      // Prints: 3
        System.out.println(358);    // Prints: 358
        System.out.println(50000);  // Prints: 50000

        // Performing mathematical calculations inside println()
        System.out.println(3 + 3);   // Prints: 6 (Addition)
        System.out.println(2 * 5);   // Prints: 10 (Multiplication)

        // Combining numbers and text using concatenation
        System.out.println("The sum of 10 and 20 is: " + (10 + 20)); // Prints: The sum of 10 and 20 is: 30
        System.out.println("Product of 4 and 5 is: " + (4 * 5));     // Prints: Product of 4 and 5 is: 20
    }
}
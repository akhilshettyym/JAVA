// Java Variables - Real-Life Examples

public class E_Examples {
    public static void main(String[] args) {

        // Student data example
        String studentName = "John Doe";
        int studentID = 15;
        int studentAge = 23;
        float studentFee = 75.25f;
        char studentGrade = 'B';

        // Printing student details
        System.out.println("Student name: " + studentName);
        System.out.println("Student ID: " + studentID);
        System.out.println("Student age: " + studentAge);
        System.out.println("Student fee: $" + studentFee);
        System.out.println("Student grade: " + studentGrade);

        System.out.println("----------------------------");

        // Rectangle Area Calculation Example
        int length = 4;
        int width = 6;
        int area = length * width; // Calculate the area of the rectangle

        // Printing rectangle dimensions and area
        System.out.println("Length is: " + length);
        System.out.println("Width is: " + width);
        System.out.println("Area of the rectangle is: " + area);
    }
}